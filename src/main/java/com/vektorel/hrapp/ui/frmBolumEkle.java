    private void bolumTabloDoldur() {
        List<Bolum> bolum = bolumService.getAll(null);
        String[][] data = new String[bolum.size()][8];
        for (int i = 0; i < bolum.size(); i++) {
            data[i][0] = bolum.get(i).getId().toString();
            data[i][1] = bolum.get(i).getKod();
            data[i][2] = bolum.get(i).getTanim();
        }

        tblBolum.setModel(new javax.swing.table.DefaultTableModel(
                data,
                new String[]{
                    "Id", "Bölüm Kodu", "Bölüm Tanımı"
                }
        ));

    }

