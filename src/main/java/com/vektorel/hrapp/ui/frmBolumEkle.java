    private void btnBolumKaydetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBolumKaydetActionPerformed
        try {
            if (lblBolumId.getText().trim().equals("")) {
                bolumService.save(new Bolum(null, txtBolumKodu.getText(), txtBolumAdi.getText()));
            } else {
                bolumService.update(new Bolum(new Long(lblBolumId.getText()), txtBolumKodu.getText(), txtBolumAdi.getText()));
            }
            bolumTabloDoldur();
            
            txtBolumKodu.setText("");
            txtBolumAdi.setText("");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnBolumKaydetActionPerformed

    private void btnBolumGuncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBolumGuncelleActionPerformed
        int secilikayit = tblBolum.getSelectedRow();
        if (secilikayit > -1) {
            String value = tblBolum.getValueAt(secilikayit, 0).toString();
            Bolum bolum = bolumService.getById(new Long(value));

            lblBolumId.setText(bolum.getId().toString());
            txtBolumKodu.setText(bolum.getKod());
            txtBolumAdi.setText(bolum.getTanim());
        }

    }//GEN-LAST:event_btnBolumGuncelleActionPerformed

    private void btnBolumSilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBolumSilActionPerformed
        int secilikayit = tblBolum.getSelectedRow();
        if (secilikayit > -1) {
            String value = tblBolum.getValueAt(secilikayit, 0).toString();
            Bolum bolum = bolumService.getById(new Long(value));
            try {
                bolumService.delete(bolum);
                bolumTabloDoldur();
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(rootPane, e.getMessage());
            }

        }
    }//GEN-LAST:event_btnBolumSilActionPerformed

