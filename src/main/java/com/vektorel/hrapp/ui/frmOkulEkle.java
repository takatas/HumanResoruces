    private void btnKaydetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKaydetActionPerformed
        try {
            Adres adres = new Adres();
            adres.setAcikAdres(txtAdres.getText());
            if (lblOkulId.getText().trim().equals("")) {
                okulService.save(new Okul(null, txtKod.getText(), txtAd.getText(), adres));
            } else {
                okulService.update(new Okul(new Long(lblOkulId.getText()), txtKod.getText(), txtAd.getText(), adres));
            }
            okulTabloyuDoldur();
            alanTemizle();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnKaydetActionPerformed

    private void btnGuncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuncelleActionPerformed
        int seciliKayit = tblOkul.getSelectedRow();
        if (seciliKayit > -1) {
            String value = tblOkul.getValueAt(seciliKayit, 0).toString();

            Okul okul = okulService.getById(new Long(value));

            lblOkulId.setText(okul.getId().toString());
            txtKod.setText(okul.getKod());
            txtAd.setText(okul.getAdi());
            txtAdres.setText(okul.getAdres().getAcikAdres());
        }
    }//GEN-LAST:event_btnGuncelleActionPerformed

