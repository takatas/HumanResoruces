    private void okulTabloyuDoldur() {
        List<Okul> okul = okulService.getAll(null);
        String[][] data = new String[okul.size()][6];
        for (int i = 0; i < okul.size(); i++) {
            data[i][0] = okul.get(i).getId().toString();
            data[i][1] = okul.get(i).getKod();
            data[i][2] = okul.get(i).getAdi();
            data[i][3] = okul.get(i).getAdres().getAcikAdres();

        }

        tblOkul.setModel(new javax.swing.table.DefaultTableModel(
                data,
                new String[]{
                    "Id", "Kod", "Ad", "Adres"
                }
        ));
    }

    private void alanTemizle() {
        txtAd.setText("");
        txtAdres.setText("");
        txtKod.setText("");
    }
